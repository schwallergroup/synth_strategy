root_data = "/home/dparm/steerable_retro/data"

fg_args = {
    "file_path": f"{root_data}/patterns/functional_groups.json",
    "value_field": "pattern",
    "key_field": "name",
}
reaction_class_args = {
    "file_path": f"{root_data}/patterns/smirks.json",
    "value_field": "smirks",
    "key_field": "name",
}
ring_smiles_args = {
    "file_path": f"{root_data}/patterns/chemical_rings_smiles.json",
    "value_field": "smiles",
    "key_field": "name",
}
functional_groups = fuzzy_dict.FuzzyDict.from_json(**fg_args)
reaction_classes = fuzzy_dict.FuzzyDict.from_json(**reaction_class_args)
ring_smiles = fuzzy_dict.FuzzyDict.from_json(**ring_smiles_args)

checker = check.Check(
    fg_dict=functional_groups, reaction_dict=reaction_classes, ring_dict=ring_smiles
)


def main(route):
    """
    This function detects a strategy involving amine to nitro functional group conversion.
    In retrosynthetic analysis, this means finding nitro to amine reductions.
    """
    found_conversion = False

    def dfs_traverse(node, depth=0):
        nonlocal found_conversion

        if node["type"] == "reaction" and "metadata" in node and "rsmi" in node["metadata"]:
            rsmi = node["metadata"]["rsmi"]
            reactants = rsmi.split(">")[0].split(".")
            product = rsmi.split(">")[-1]

            # Check for nitro reduction reaction
            if checker.check_reaction("Reduction of nitro groups to amines", rsmi):
                print(f"Found nitro reduction reaction at depth {depth}: {rsmi}")
                found_conversion = True
                return

            # In retrosynthesis, we're looking for nitro in reactants and amine in product
            nitro_in_reactants = False
            for r in reactants:
                if checker.check_fg("Nitro group", r):
                    print(f"Found nitro group in reactant at depth {depth}: {r}")
                    nitro_in_reactants = True
                    break

            if nitro_in_reactants:
                # Check for amine in product
                if (
                    checker.check_fg("Primary amine", product)
                    or checker.check_fg("Secondary amine", product)
                    or checker.check_fg("Tertiary amine", product)
                    or checker.check_fg("Aniline", product)
                ):
                    print(f"Found amine in product at depth {depth}: {product}")
                    found_conversion = True

        # Continue traversal
        for child in node.get("children", []):
            dfs_traverse(child, depth + 1)

    # Start traversal from the root
    dfs_traverse(route)
    return found_conversion
