ring_smiles_args = {
    "file_path": "/home/dparm/steerable_retro/data/patterns/chemical_rings_smiles.json",
    "value_field": "smiles",
    "key_field": "name",
}
functional_groups = fuzzy_dict.FuzzyDict.from_json(**fg_args)
reaction_classes = fuzzy_dict.FuzzyDict.from_json(**reaction_class_args)
ring_smiles = fuzzy_dict.FuzzyDict.from_json(**ring_smiles_args)

checker = check.Check(
    fg_dict=functional_groups, reaction_dict=reaction_classes, ring_dict=ring_smiles
)


def main(route):
    """
    This function detects if the synthetic route involves a cyclopropanation reaction.
    """
    cyclopropanation_found = False

    def dfs_traverse(node):
        nonlocal cyclopropanation_found

        if node["type"] == "reaction" and "metadata" in node and "rsmi" in node["metadata"]:
            try:
                rsmi = node["metadata"]["rsmi"]
                reactants = rsmi.split(">")[0].split(".")
                product = rsmi.split(">")[-1]

                # Check if product contains a cyclopropane
                if checker.check_ring("cyclopropane", product):
                    # Check if cyclopropane was already in reactants
                    cyclopropane_in_reactants = False
                    for reactant in reactants:
                        if checker.check_ring("cyclopropane", reactant):
                            cyclopropane_in_reactants = True
                            break

                    if not cyclopropane_in_reactants:
                        print(f"Cyclopropanation detected in reaction: {rsmi}")
                        cyclopropanation_found = True
            except Exception as e:
                print(f"Error processing reaction: {e}")

        # Continue traversing
        for child in node.get("children", []):
            dfs_traverse(child)

    # Start traversal
    dfs_traverse(route)
    return cyclopropanation_found
