def main(route):
    """
    This function detects if the synthetic route follows a linear synthesis strategy
    rather than a convergent one.
    """
    is_linear = True

    def dfs_traverse(node, depth=0):
        nonlocal is_linear

        if node["type"] == "reaction":
            if "rsmi" in node.get("metadata", {}):
                rsmi = node["metadata"]["rsmi"]
                reactants = rsmi.split(">")[0].split(".")

                # If there are more than 2 reactants, it's likely a convergent step
                if len(reactants) > 2:
                    is_linear = False
                    print(
                        f"Convergent step detected at depth {depth} with {len(reactants)} reactants"
                    )

        # Traverse children
        for child in node.get("children", []):
            dfs_traverse(child, depth + 1)

    # Start traversal
    dfs_traverse(route)

    return is_linear
