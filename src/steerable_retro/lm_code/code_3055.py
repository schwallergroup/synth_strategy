def main(route):
    """
    This function detects if the synthetic route involves formation of an alcohol
    in the late stages of the synthesis (depth <= 1).
    """
    late_stage_alcohol_formed = False

    def dfs_traverse(node, depth=0):
        nonlocal late_stage_alcohol_formed

        if node["type"] == "reaction" and depth <= 1:
            if "metadata" in node and "rsmi" in node["metadata"]:
                rsmi = node["metadata"]["rsmi"]
                reactants = rsmi.split(">")[0].split(".")
                product = rsmi.split(">")[-1]

                # Check for ketone/aldehyde -> alcohol pattern
                if any("C(=O)" in r or "CH(=O)" in r for r in reactants) and "[OH:" in product:
                    late_stage_alcohol_formed = True
                    print(f"Found late-stage alcohol formation at depth {depth}: {rsmi}")

        # Traverse children with incremented depth
        for child in node.get("children", []):
            dfs_traverse(child, depth + 1)

    # Start traversal
    dfs_traverse(route)

    print(f"Late-stage alcohol formation detected: {late_stage_alcohol_formed}")
    return late_stage_alcohol_formed
