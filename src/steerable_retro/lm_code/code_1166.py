def main(route):
    """Helper function to count the total number of reaction nodes"""
    reaction_count = 0

    def dfs_count(node):
        nonlocal reaction_count
        if node["type"] == "reaction":
            reaction_count += 1
        for child in node.get("children", []):
            dfs_count(child)

    dfs_count(route)
    return reaction_count
