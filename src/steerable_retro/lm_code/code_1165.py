def main(route):
    """Helper function to determine the maximum depth of the synthesis route"""
    max_depth = 0

    def dfs_max_depth(node, depth=0):
        nonlocal max_depth
        max_depth = max(max_depth, depth)
        for child in node.get("children", []):
            dfs_max_depth(child, depth + 1)

    dfs_max_depth(route)
    return max_depth
