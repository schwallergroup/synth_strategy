ring_smiles = fuzzy_dict.FuzzyDict.from_json(**ring_smiles_args)

checker = check.Check(
    fg_dict=functional_groups, reaction_dict=reaction_classes, ring_dict=ring_smiles
)


def main(route):
    """
    This function detects if the synthesis involves multiple types of nitrogen heterocycles
    in the final product.
    """
    # List of nitrogen heterocycles to check
    nitrogen_heterocycles = [
        "pyrrole",
        "pyridine",
        "pyrazole",
        "imidazole",
        "oxazole",
        "thiazole",
        "pyrimidine",
        "pyrazine",
        "pyridazine",
        "triazole",
        "tetrazole",
        "pyrrolidine",
        "piperidine",
        "piperazine",
        "morpholine",
        "thiomorpholine",
        "aziridine",
        "azetidine",
        "azepane",
        "diazepane",
        "indole",
        "quinoline",
        "isoquinoline",
        "purine",
        "carbazole",
        "acridine",
        "thiazolidine",
        "oxazolidine",
        "isoxazole",
        "isothiazole",
        "oxadiazole",
        "thiadiazole",
        "benzoxazole",
        "benzothiazole",
        "benzimidazole",
        "pteridin",
        "phenothiazine",
        "phenoxazine",
        "pyrroline",
        "imidazolidine",
        "indazole",
        "benzotriazole",
    ]

    detected_heterocycles = set()

    def dfs_traverse(node, depth=0):
        if node["type"] == "mol" and depth == 0:  # Final product
            mol_smiles = node["smiles"]
            print(f"Checking final product: {mol_smiles}")

            # Check for different nitrogen heterocycles
            for heterocycle in nitrogen_heterocycles:
                if checker.check_ring(heterocycle, mol_smiles):
                    detected_heterocycles.add(heterocycle)
                    print(f"{heterocycle} detected in final product")

        # Continue traversing
        for child in node.get("children", []):
            dfs_traverse(child, depth + 1)

    # Start traversal
    dfs_traverse(route)

    print(f"Total nitrogen heterocycles detected: {len(detected_heterocycles)}")
    print(f"Detected heterocycles: {', '.join(detected_heterocycles)}")

    return len(detected_heterocycles) >= 2  # At least 2 different heterocycles
