from pathlib import Path
root_data = Path(__file__).parent.parent

fg_args = {
    "file_path": f"{root_data}/patterns/functional_groups.json",
    "value_field": "pattern",
    "key_field": "name",
}
reaction_class_args = {
    "file_path": f"{root_data}/patterns/smirks.json",
    "value_field": "smirks",
    "key_field": "name",
}
ring_smiles_args = {
    "file_path": f"{root_data}/patterns/chemical_rings_smiles.json",
    "value_field": "smiles",
    "key_field": "name",
}
functional_groups = fuzzy_dict.FuzzyDict.from_json(**fg_args)
reaction_classes = fuzzy_dict.FuzzyDict.from_json(**reaction_class_args)
ring_smiles = fuzzy_dict.FuzzyDict.from_json(**ring_smiles_args)

checker = check.Check(
    fg_dict=functional_groups, reaction_dict=reaction_classes, ring_dict=ring_smiles
)


def main(route) -> Tuple[bool, Dict]:
    """
    This function detects a synthetic strategy involving hydrogenation,
    such as the saturation of an aromatic ring or the reduction of a
    carbon-carbon double or triple bond.
    """
    findings_template = {
        "atomic_checks": {
            "named_reactions": [],
            "ring_systems": [],
            "functional_groups": []
        },
        "structural_constraints": []
    }
    findings_json = copy.deepcopy(findings_template)

    has_dehydrogenation = False

    def dfs_traverse(node, depth=0, max_depth=-1):
        nonlocal has_dehydrogenation, findings_json

        if has_dehydrogenation:
            return

        if node["type"] == "reaction" and "metadata" in node and "mapped_reaction_smiles" in node["metadata"]:
            rsmi = node["metadata"]["mapped_reaction_smiles"]

            # This block correctly identifies hydrogenation reactions, which was the
            # actual (though mislabeled) behavior of the original function's most
            # reliable checks.
            if checker.check_reaction("Arene hydrogenation", rsmi):
                has_dehydrogenation = True
                findings_json["atomic_checks"]["named_reactions"].append("Arene hydrogenation")
                return

            if checker.check_reaction(
                "Hydrogenation (double to single)", rsmi
            ):
                has_dehydrogenation = True
                findings_json["atomic_checks"]["named_reactions"].append("Hydrogenation (double to single)")
                return
            
            if checker.check_reaction("Hydrogenation (triple to double)", rsmi):
                has_dehydrogenation = True
                findings_json["atomic_checks"]["named_reactions"].append("Hydrogenation (triple to double)")
                return

        for child in node.get("children", []):
            # New logic for depth calculation
            new_depth = depth
            if node["type"] != "reaction": # If current node is chemical, depth increases
                new_depth = depth + 1
            # If current node is reaction, depth remains the same
            dfs_traverse(child, new_depth, max_depth)

    dfs_traverse(route)
    return has_dehydrogenation, findings_json
